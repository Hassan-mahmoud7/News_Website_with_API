<script>
     $(document).on('change', '#profile-image', function(e) {
            e.preventDefault();
            var file = this.files[0];
            if (file) {
                var reader = new FileReader();
                reader.onload = function(e) {
                    // تحديث الصورة المعروضة
                    $('#profile_img').attr('src', e.target.result);
                }
                reader.readAsDataURL(file);
            }
        });
</script>
